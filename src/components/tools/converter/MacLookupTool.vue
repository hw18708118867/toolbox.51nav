  if (!result.value) return [];
  const mac = result.value.normalized;
  const hex = mac.replace(/:/g, '');
  return [
    { label: '冒号分隔 (xx:xx:xx:xx:xx:xx)', value: hex.match(/.{2}/g)!.join(':').toUpperCase() },
    { label: '短横线分隔 (xx-xx-xx-xx-xx-xx)', value: hex.match(/.{2}/g)!.join('-').toUpperCase() },
    { label: 'Cisco 格式 (xxxx.xxxx.xxxx)', value: hex.match(/.{4}/g)!.join('.').toUpperCase() },
    { label: '无分隔符', value: hex.toUpperCase() },
  ];
});
