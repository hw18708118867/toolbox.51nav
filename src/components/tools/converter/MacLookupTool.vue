function normalizeMac(mac: string): string {
  // Remove all non-hex characters and convert to uppercase
  const cleaned = mac.replace(/[^0-9A-Fa-f]/g, '').toUpperCase();
  if (cleaned.length !== 12) return '';
  return cleaned.match(/.{2}/g)!.join(':');
}

function extractOui(mac: string): string {
  const cleaned = mac.replace(/[^0-9A-Fa-f]/g, '').toUpperCase();
  if (cleaned.length >= 6) return cleaned.slice(0, 6);
  return cleaned;
}

function lookupVendor(oui: string): string {
  // First check wellKnownOuis by colon format
  const ouiColon = oui.match(/.{2}/g)!.join(':');
  if (wellKnownOuis[ouiColon]) return wellKnownOuis[ouiColon];

  // Check ouiDatabase
  if (ouiDatabase[oui]) return ouiDatabase[oui];

  // Check with first byte masked (for local/private addresses)
  const firstByte = parseInt(oui.slice(0, 2), 16);
  if (firstByte & 0x02) return '本地管理地址 (Locally Administered)';

  return '未在数据库中查找到该厂商信息';
}

function getAddressType(firstByte: number): string {
  if (firstByte & 0x01) return '组播地址 (Multicast)';
  return '单播地址 (Unicast)';
}

function lookup() {
  error.value = '';
  result.value = null;

  const input = macInput.value.trim();
  if (!input) {
    error.value = '请输入 MAC 地址';
    return;
  }

  const normalized = normalizeMac(input);
  if (!normalized) {
    error.value = '无效的 MAC 地址格式：需要 12 个十六进制字符';
    return;
  }

  const ouiRaw = extractOui(input);
  const oui = ouiRaw.match(/.{2}/g)!.join(':');
  const vendor = lookupVendor(ouiRaw);
  const firstByte = parseInt(ouiRaw.slice(0, 2), 16);
  const addressType = getAddressType(firstByte);

  result.value = {
    oui,
    vendor,
    addressType,
    normalized,
  };
}

